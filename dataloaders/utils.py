# The following operations are called color augmentation
def adjust_brightness(input_tensor, brightness_factor):
    """
    Adjust brightness of tensor image. The tensor is expected to have dimensions (C, D, H, W)
    """
    adjusted_tensor = input_tensor * brightness_factor
    return adjusted_tensor


def adjust_contrast(input_tensor, contrast_factor):
    """
    Adjust contrast of tensor image. The tensor is expected to have dimensions (C, D, H, W)
    """
    mean = torch.mean(input_tensor, dim=(1, 2, 3), keepdim=True)  # compute mean over (D, H, W)
    adjusted_tensor = (input_tensor - mean) * contrast_factor + mean
    return adjusted_tensor


def adjust_color(input_tensor, brightness_factor, contrast_factor):
    """
    :param input_tensor: (bs, 1, H, W, D)
    :param brightness_factor: tensor: float
    :param contrast_factor: tensor: float
    :return:
    """
    bs, _, _, _, _ = input_tensor.shape
    adjust_tensors = torch.zeros_like(input_tensor)
    fn_idx = torch.randperm(2)

    for i in range(bs):
        img = input_tensor[i]  # (1, H, W, D)
        for idx in fn_idx:
            if idx == 0:
                img = adjust_brightness(img, brightness_factor)
            if idx == 1:
                img = adjust_contrast(img, contrast_factor)

        adjust_tensors[i] = img

    return adjust_tensors


class CenterCrop(object):
    def __init__(self, output_size):
        self.output_size = output_size

    def __call__(self, sample):
        image, label = sample['image'], sample['label']

        # pad the sample if necessary
        if label.shape[0] <= self.output_size[0] or label.shape[1] <= self.output_size[1] or label.shape[2] <= \
                self.output_size[2]:
            pw = max((self.output_size[0] - label.shape[0]) // 2 + 3, 0)
            ph = max((self.output_size[1] - label.shape[1]) // 2 + 3, 0)
            pd = max((self.output_size[2] - label.shape[2]) // 2 + 3, 0)
            image = np.pad(image, [(pw, pw), (ph, ph), (pd, pd)], mode='constant', constant_values=0)
            label = np.pad(label, [(pw, pw), (ph, ph), (pd, pd)], mode='constant', constant_values=0)

        (w, h, d) = image.shape

        w1 = int(round((w - self.output_size[0]) / 2.))
        h1 = int(round((h - self.output_size[1]) / 2.))
        d1 = int(round((d - self.output_size[2]) / 2.))

        label = label[w1:w1 + self.output_size[0], h1:h1 + self.output_size[1], d1:d1 + self.output_size[2]]
        image = image[w1:w1 + self.output_size[0], h1:h1 + self.output_size[1], d1:d1 + self.output_size[2]]

        return {'image': image, 'label': label}


