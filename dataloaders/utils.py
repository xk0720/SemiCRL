def split_ids(len_ids, test_size, labeled_size):
    train_indices, test_indices = train_test_split(
        np.linspace(0, len_ids - 1, len_ids).astype('int'),
        test_size=test_size,
        random_state=42,
    )

    unlabeled_indices, labeled_indices = train_test_split(
        train_indices,
        test_size=labeled_size,
        random_state=42,
    )

    return unlabeled_indices, labeled_indices, test_indices